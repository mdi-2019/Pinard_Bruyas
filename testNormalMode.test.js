
test('adds 1 + 2 to equal 3', () => {
  expect(parseInt('1') + parseInt('2')).toBe(3)
})

test('substract 5 - 2 to equal 3', () => {
  expect(parseInt('5') - parseInt('2')).toBe(3)
})

test('multiply 10 * 2 to equal 20', () => {
  expect(parseInt('10') * parseInt('2')).toBe(20)
})

test('divid 100 / 2 to equal 50', () => {
  expect(parseInt('100') / parseInt('2')).toBe(50)
})
